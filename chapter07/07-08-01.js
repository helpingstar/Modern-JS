// 문자열 연결 연산자
console.log('1' + 2); // 12
console.log(1 + '2'); // 12

// true는 1로 타입이 변환된다.
console.log(true + 1); // 2

// false는 0으로 타입이 변환된다.
console.log(false + 1); // 1

// null은 0으로 타입이 변환된다.
console.log(null + 1); // 1

// undefined는 숫자로 타입이 변환되지 않는다.
console.log(undefined + 1); // NaN
